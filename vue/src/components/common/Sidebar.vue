<template>
    <div class="sidebar">
        <el-menu
            class="sidebar-el-menu"
            :default-active="onRoutes"
            :collapse="collapse"
            background-color="#324157"
            text-color="#bfcbd9"
            active-text-color="#20a0ff"
            unique-opened
            router
        >
            <template v-for="item in items">
                <template v-if="item.subs">
                    <el-submenu :index="item.index" :key="item.index">
                        <template slot="title">
                            <i :class="item.icon"></i>
                            <span slot="title">{{ item.title }}</span>
                        </template>
                        <template v-for="subItem in item.subs">
                            <el-submenu
                                v-if="subItem.subs"
                                :index="subItem.index"
                                :key="subItem.index"
                            >
                                <template slot="title">{{ subItem.title }}</template>
                                <el-menu-item
                                    v-for="(threeItem,i) in subItem.subs"
                                    :key="i"
                                    :index="threeItem.index"
                                >{{ threeItem.title }}</el-menu-item>
                            </el-submenu>
                            <el-menu-item
                                v-else
                                :index="subItem.index"
                                :key="subItem.index"
                            >{{ subItem.title }}</el-menu-item>
                        </template>
                    </el-submenu>
                </template>
                <template v-else>
                    <el-menu-item :index="item.index" :key="item.index">
                        <i :class="item.icon"></i>
                        <span slot="title">{{ item.title }}</span>
                    </el-menu-item>
                </template>
            </template>
        </el-menu>
    </div>
</template>

<script>
import bus from '../common/bus';
export default {
    data() {
        return {
            collapse: false,
            items: [
                {
                    icon: 'el-icon-lx-home',
                    index: 'dashboard',
                    title: '课程首页'
                },
                {
                    icon: 'el-icon-lx-copy',
                    index: 'tabs',
                    title: '消息通知'
                },
                {
                    icon: 'el-icon-lx-calendar',
                    index: 'note',
                    title: '学习笔记',
                },
                
                //参与考试
                {
                    icon:'el-icon-tickets',
                    index:'',
                    title:'考试',
                    subs:[
                        {
                            index:'dailyEx',
                            title:'测试'
                        },
                        {
                            index:'midEx',
                            title:'期中考试'
                        },
                        {
                            index:'finEx',
                            title:'期末考试'
                        }

                    ]
                },

                {
                    //学习计划
                    icon: 'el-icon-s-order',
                    index: 'plans',
                    title: '学习计划'
                },
                {
                    //学习数据    
                    icon: 'el-icon-pie-chart',
                    index: 'charts',
                    title: '数据分析'
                },
               
               
            ]
        };
    },
    computed: {
        onRoutes() {
            return this.$route.path.replace('/', '');
        }
    },
    created() {
        // 通过 Event Bus 进行组件间通信，来折叠侧边栏
        bus.$on('collapse', msg => {
            this.collapse = msg;
            bus.$emit('collapse-content', msg);
        });
    }
};
</script>

